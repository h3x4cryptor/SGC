CREATE TABLE `i_users` (
    `id` int NOT NULL AUTO_INCREMENT,
    `username` varchar(64) DEFAULT NULL,
    `email` varchar(64) DEFAULT NULL,
    `pass` varchar(255) DEFAULT NULL,
    `salt` varchar(255) DEFAULT NULL,
    `w_id` INT(64),
    `r_id` INT(64),
    `UUID` INT(64),
    `status` bool DEFAULT 0,
    `joined` TIMESTAMP,
    PRIMARY KEY (`id`)
    );
CREATE TABLE i_finance (
      id INT(11) UNSIGNED AUTO_INCREMENT PRIMARY KEY,
      u_id INT(11),
      w_id varchar(64) DEFAULT NULL,
      walletname VARCHAR(30) NULL,
      wallettype VARCHAR(30) NULL,
      walletpin VARCHAR(255),
      walletbalance Double,
      w_salt VARCHAR(255),
      username VARCHAR(30) NULL,
      created_on TIMESTAMP
      );
CREATE TABLE `f_loan` (
    `id` INT(11) UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    `username` varchar(64) DEFAULT NULL,
    `flo_name` varchar(64) DEFAULT NULL,
    `flot_amount` Double,
    `flomi_amount` Double,
    `flo_period` INT(64),
    `w_id` INT(11) NOT NULL,
    `UUID` INT(64),
    `status` bool DEFAULT 0,
    `created` TIMESTAMP
    );
    flo_name | Finance Lona name
    flot_amount | Finance Lona total amount
    flomi_amount | Finance Lona monthly instalments
    flo_period | Finance Lona Period

CREATE TABLE `i_comments` (
    `id` INT(11) UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    `username` varchar(64) DEFAULT NULL,
    `c_data` varchar(255) DEFAULT NULL,
    `sc_data` varchar(255) DEFAULT NULL,
    `c_date` TIMESTAMP
    );

CREATE TABLE `i_chat` (
    `id` INT(9) NOT NULL AUTO_INCREMENT , 
    `usrname` VARCHAR(255) NOT NULL , 
    `color` VARCHAR(6) NOT NULL , 
    `chattext` TEXT NOT NULL , 
    `chattime` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP , 
    PRIMARY KEY (`id`) 
    ) ENGINE = MyISAM CHARACTER SET utf8 COLLATE utf8_unicode_ci;



CREATE TABLE `wp_slack` (
    `id` INT(9) NOT NULL AUTO_INCREMENT , 
    `cmd` VARCHAR(255) NOT NULL , 
    `php` TEXT NOT NULL , 
    `html` TEXT NOT NULL , 
    `sql` TEXT NOT NULL , 
    `clr` TEXT NOT NULL , 
    `txt` TEXT NOT NULL , 
    `img` TEXT NOT NULL , 
    `time` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP , 
    PRIMARY KEY (`id`) 
    ) ENGINE = MyISAM CHARACTER SET utf8 COLLATE utf8_unicode_ci;




